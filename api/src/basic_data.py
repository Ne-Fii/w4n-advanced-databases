def get_dates(year: str):
    months = [i for i in range(1, 13)]
    days = [i for i in range(1, 20)]
    l_dates = [ f"{year}-{str(month).rjust(2, '0')}-{str(day).rjust(2, '0')}" for month in months for day in days ]
    return l_dates


